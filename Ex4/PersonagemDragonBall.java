public class PersonagemDragonBall {
    String nome;
    int idade;
    String sexo;
    int ki;
    String poderEspecial;

    public PersonagemDragonBall(String nome, int idade, String sexo, int ki, String poderEspecial) {
        this.nome = nome;
        this.idade = idade;
        this.sexo = sexo;
        this.ki = ki;
        this.poderEspecial = poderEspecial;
    }
}